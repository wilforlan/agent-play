import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const outPath = join(
  root,
  "packages/play-ui/src/passphrase-wordlist-data.ts"
);

const BASE =
  "https://raw.githubusercontent.com/dariusk/corpora/master/data/words";

const SOURCES = [
  {
    bucket: "nouns",
    url: `${BASE}/nouns.json`,
    extract: (j) => j.nouns,
  },
  {
    bucket: "nouns",
    url: `${BASE}/personal_nouns.json`,
    extract: (j) => j.personalNouns,
  },
  {
    bucket: "verbs",
    url: `${BASE}/verbs.json`,
    extract: (j) => j.verbs,
  },
  {
    bucket: "verbs",
    url: `${BASE}/infinitive_verbs.json`,
    extract: (j) => (Array.isArray(j) ? j : []),
  },
  {
    bucket: "verbs",
    url: `${BASE}/state_verbs.json`,
    extract: (j) =>
      Array.isArray(j) ? j.map((row) => row.word).filter(Boolean) : [],
  },
  {
    bucket: "adjectives",
    url: `${BASE}/adjs.json`,
    extract: (j) => j.adjs,
  },
  {
    bucket: "adverbs",
    url: `${BASE}/adverbs.json`,
    extract: (j) => j.adverbs,
  },
];

function normalizeWord(raw) {
  const s = String(raw).trim().toLowerCase();
  if (s.length < 2 || s.length > 32) {
    return null;
  }
  if (!/^[a-z]+(?:-[a-z]+)*$/.test(s)) {
    return null;
  }
  return s;
}

function uniqueSorted(arr) {
  return [...new Set(arr)].sort((a, b) => a.localeCompare(b));
}

async function fetchJson(url) {
  const res = await fetch(url);
  if (!res.ok) {
    throw new Error(`fetch ${url}: ${res.status}`);
  }
  return res.json();
}

async function main() {
  const buckets = {
    nouns: [],
    verbs: [],
    adjectives: [],
    adverbs: [],
  };

  for (const src of SOURCES) {
    const json = await fetchJson(src.url);
    const raw = src.extract(json);
    if (!Array.isArray(raw)) {
      throw new Error(`extract() did not return array for ${src.url}`);
    }
    for (const w of raw) {
      const n = normalizeWord(w);
      if (n !== null) {
        buckets[src.bucket].push(n);
      }
    }
  }

  buckets.nouns = uniqueSorted(buckets.nouns);
  buckets.verbs = uniqueSorted(buckets.verbs);
  buckets.adjectives = uniqueSorted(buckets.adjectives);
  buckets.adverbs = uniqueSorted(buckets.adverbs);

  const total =
    buckets.nouns.length +
    buckets.verbs.length +
    buckets.adjectives.length +
    buckets.adverbs.length;

  if (total < 3000) {
    throw new Error(
      `expected at least 3000 words after normalization, got ${total}`
    );
  }

  const header = `/**
 * Auto-generated by scripts/build-passphrase-wordlist.mjs — do not edit by hand.
 * Word sources: dariusk/corpora (CC0), https://github.com/dariusk/corpora
 * Files: nouns, personal_nouns, verbs, infinitive_verbs, state_verbs, adjs, adverbs
 * Normalization: lowercase, a-z and hyphenated tokens only, length 2–32, deduplicated per bucket.
 * Total: ${total}
 */
`;

  const body = `export const passphraseNouns = ${JSON.stringify(buckets.nouns)} as const;

export const passphraseVerbs = ${JSON.stringify(buckets.verbs)} as const;

export const passphraseAdjectives = ${JSON.stringify(buckets.adjectives)} as const;

export const passphraseAdverbs = ${JSON.stringify(buckets.adverbs)} as const;

export const passphraseWordlistTotalCount = ${total} as const;
`;

  writeFileSync(outPath, header + body, "utf8");
  console.log(
    `Wrote ${outPath}: nouns=${buckets.nouns.length} verbs=${buckets.verbs.length} adjectives=${buckets.adjectives.length} adverbs=${buckets.adverbs.length} total=${total}`
  );
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
